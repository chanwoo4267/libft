#include "libft.h"

int	ft_atoi(const char *str)
{
	int		result;
	int		sign;
	size_t	i;

	i = 0;
	result = 0;
	sign = 1;
	if (str[i] == '+' || str[i] == '-')
	{
		i++;
		if (str[i] == '-')
			sign *= -1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result);
}
